#include <stdio.h>

const char T[12] = {
    'A',
    'B',
    'C',
    'D',
    'E',
    'F',
    'G',
    'H',
    'I',
    'J',
    'K',
    'L'
};

int main(void)
{
    int jhr;
    scanf("%d", &jhr);
    jhr -= 4;
    if(jhr < 0) jhr = 60 + (jhr % 60);

    int gan = jhr % 10,
        ji  = jhr % 12;
    printf("%c%d", T[ji], gan);

    return 0;
}