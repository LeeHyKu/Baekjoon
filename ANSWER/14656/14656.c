#include <stdio.h>

int main(void)
{
    int sch,
        erg = 0;
    scanf("%d", &sch);
    for(int i = 1; i <= sch; i++)
    {
        int anh;
        scanf("%d", &anh);
        if(anh != i) erg++;
    }
    printf("%d", erg);
    return 0;
}